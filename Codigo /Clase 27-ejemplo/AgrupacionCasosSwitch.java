class AgrupacionCasosSwitch{

   public static void main(String[] args) {
      int opcion = 0 ;
      switch (opcion) {
       case 0:
       case 1:
       case 2:
          System.out.println("opcion 0 ,1 y 2");
          break;
       case 3:
          System.out.println("opcion 3");
       break;
       default:
          System.out.println("opcion 4");
       break;
      }
   }

}