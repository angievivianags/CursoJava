class OperadoresDeComparacion{

   public static void main(String[] args) {
      double sueldo1 = 1000.1234543;
      double sueldo2 = 5123213.12323;

      System.out.println(sueldo1 > sueldo2);
      System.out.println(sueldo1 < sueldo2);
      System.out.println(sueldo1 >= sueldo2);
      System.out.println(sueldo1 <= sueldo2);
      System.out.println(sueldo1 != sueldo2);
      System.out.println(sueldo1 == sueldo2);

   }
}