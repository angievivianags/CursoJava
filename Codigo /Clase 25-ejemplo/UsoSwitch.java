class UsoSwitch{

   public static void main(String[] args) {
     int opcion = 0 ;
     switch (opcion) {
      case 0:
         System.out.println("opcion 0");
         break;
      case 1:
         System.out.println("opcion 1");
         break;
      case 2:
         System.out.println("opcion 2");
      break;
      case 3:
         System.out.println("opcion 3");
      break;
      default:
         System.out.println("opcion 4");
      break;
     }
   }

}