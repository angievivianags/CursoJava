class Operadores{

   public static void main(String[] args) {
      //Operadores Aritmeticos 
      
      //Suma
      int x = 10+20;
      System.out.println("Suma 10+0 ="+x);
      
      //Resta
      int y = 10-20;
      System.out.println("Resta 10-20="+y);
      
      //Multiplicacion 
      int z = 10*20;
      System.out.println("Multiplicacion 10*20="+z);
      
      //Division
      int a = 10/20;
      System.out.println("Division 10/20="+a);

      //Modulo
      int b = 10%20;
      System.out.println("Modulo 10%20="+b);
   }
}