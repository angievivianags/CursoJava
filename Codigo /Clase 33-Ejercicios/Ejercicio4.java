class Ejercicio4{

   public static void main(String[] args) {
    
      System.out.println("Imprimir Cuadrado");
      for (int i = 0; i < 5; i++) {
            System.out.println("***********");
      }

      System.out.println("Imprimir triangulo");
      for (int i = 0; i < 5; i++) {
         for (int j = 0; j < 11; j++) {
            System.out.print("*");
         }
         System.out.println();
      }
   }

}