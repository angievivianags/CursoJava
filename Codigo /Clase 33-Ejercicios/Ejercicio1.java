class Ejercicio1{

   public static void main(String[] args) {
      for (int i = 0; i <= 20 ; i++) {
         System.out.println("Imprimiendo La Tabla del " + i);
         for (int j = 0; j <= 20 ; j++) {
            System.out.println(i + " x " + j +" = " +i*j);
         }
         System.out.println();
      }
   }

}